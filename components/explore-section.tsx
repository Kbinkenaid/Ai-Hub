const groupedTools = {
  general: generalTools,
  coding: codingTools,
  reasoning: reasoningTools,
  cli: cliTools,
  cybersecurity: cybersecurityTools,
  academia: academiaTools,
  documentation: documentationTools,
  "image-video": imageVideoTools,
  "music-audio": musicAudioTools,
  presentations: presentationTools,
  design: designTools,
  productivity: productivityTools,
  "video-editing": videoEditingTools,
  "content-creation": contentCreationTools,
  "developer-platform": developerPlatformTools,
} as const

const ExploreSection = () => {
  const [activeTab, setActiveTab] = useState<keyof typeof groupedTools>("general")
  const [searchTerm, setSearchTerm] = useState("")
  const [selectedCategory, setSelectedCategory] = useState("All")
  const [selectedPricing, setSelectedPricing] = useState("All")
  const [sortBy, setSortBy] = useState("trending")

  const handleVisitTool = (domain: string) => {
    window.open(`https://${domain}`, "_blank")
  }

  const count = useMemo(() => {
    return (Object.keys(groupedTools) as Array<keyof typeof groupedTools>).reduce(
      (acc, key) => {
        acc[key] = groupedTools[key].length
        return acc
      },
      {} as Record<keyof typeof groupedTools, number>,
    )
  }, [])

  useEffect(() => {
    setSelectedCategory("All")
  }, [activeTab])

  const categoriesForActiveTab = useMemo(() => {
    const tools = groupedTools[activeTab] || []
    const uniqueCategories = [...new Set(tools.map((tool) => tool.category))]
    return ["All", ...uniqueCategories.sort()]
  }, [activeTab])

  const filteredAndSortedTools = useMemo(() => {
    const toolsForActiveTab = groupedTools[activeTab] || []

    const filtered = toolsForActiveTab.filter((tool) => {
      const searchTermLower = searchTerm.toLowerCase()

      const categoryMatch = selectedCategory === "All" || tool.category === selectedCategory
      const pricingMatch = selectedPricing === "All" || tool.pricing === selectedPricing
      const searchMatch =
        tool.name.toLowerCase().includes(searchTermLower) ||
        tool.shortDescription.toLowerCase().includes(searchTermLower) ||
        tool.description.toLowerCase().includes(searchTermLower) ||
        (tool.tags && tool.tags.some((tag: string) => tag.toLowerCase().includes(searchTermLower)))

      return categoryMatch && pricingMatch && searchMatch
    })

    filtered.sort((a, b) => {
      if (sortBy === "trending") {
        return (b.trending ? 1 : 0) - (a.trending ? 1 : 0) || b.rating - a.rating
      }
      if (sortBy === "rating") {
        return b.rating - a.rating
      }
      if (sortBy === "users") {
        const normalizeUsers = (users: string) => {
          const multiplier = users.includes("M") ? 1000000 : users.includes("K") ? 1000 : 1
          const numberPart = Number.parseFloat(users.replace(/[MK+]/g, ""))
          return numberPart * multiplier
        }
        return normalizeUsers(b.users) - normalizeUsers(a.users)
      }
      return 0
    })

    return filtered
  }, [activeTab, searchTerm, selectedCategory, selectedPricing, sortBy])

  const getPricingColor = (pricing: string) => {
    switch (pricing) {
      case "Free":
        return "bg-green-500 text-white"
      case "Freemium":
        return "bg-blue-500 text-white"
      case "Paid":
        return "bg-orange-500 text-white"
      case "Enterprise":
        return "bg-purple-500 text-white"
      case "Pay-per-use":
        return "bg-yellow-500 text-white"
      default:
        return "bg-gray-500 text-white"
    }
  }

  return (
    <section className="min-h-screen bg-black text-white py-16">
      <div className="container mx-auto px-4">
        {/* Header */}
        <div className="text-center mb-12">
          <h2 className="text-4xl font-bold text-white mb-4">Explore AI Tools</h2>
          <p className="text-gray-400 ">
            Discover the best AI tools for your needs. Browse by category, pricing, and more.
          </p>
        </div>

        {/* Search and Filters */}
        <div className="flex flex-col md:flex-row justify-between items-center mb-8">
          <div className="w-full md:w-1/3 mb-4 md:mb-0">
            <input
              type="text"
              placeholder="Search tools..."
              className="w-full px-4 py-3 rounded-lg bg-gray-900 border border-gray-700 text-white placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-transparent"
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
            />
          </div>

          <div className="flex items-center space-x-4">
            {/* Category Filter */}
            <select
              className="px-4 py-3 rounded-lg bg-gray-900 border border-gray-700 text-white focus:outline-none focus:ring-2 focus:ring-blue-500"
              value={selectedCategory}
              onChange={(e) => setSelectedCategory(e.target.value)}
            >
              {categoriesForActiveTab.map((category) => (
                <option key={category} value={category}>
                  {category}
                </option>
              ))}
            </select>

            {/* Pricing Filter */}
            <select
              className="px-4 py-3 rounded-lg bg-gray-900 border border-gray-700 text-white focus:outline-none focus:ring-2 focus:ring-blue-500"
              value={selectedPricing}
              onChange={(e) => setSelectedPricing(e.target.value)}
            >
              <option value="All">All Pricing</option>
              <option value="Free">Free</option>
              <option value="Freemium">Freemium</option>
              <option value="Paid">Paid</option>
              <option value="Enterprise">Enterprise</option>
              <option value="Pay-per-use">Pay-per-use</option>
            </select>

            {/* Sort By */}
            <select
              className="px-4 py-3 rounded-lg bg-gray-900 border border-gray-700 text-white focus:outline-none focus:ring-2 focus:ring-blue-500"
              value={sortBy}
              onChange={(e) => setSortBy(e.target.value)}
            >
              <option value="trending">Trending</option>
              <option value="rating">Rating</option>
              <option value="users">Users</option>
            </select>
          </div>
        </div>

        {/* Tabs */}
        <div className="mb-8">
          <div className="flex flex-wrap gap-2 mb-8">
            {(Object.keys(groupedTools) as Array<keyof typeof groupedTools>).map((tab) => (
              <button
                key={tab}
                className={`px-4 py-2 rounded-full font-medium transition-colors text-lg ${
                  activeTab === tab
                    ? "bg-blue-600 text-white"
                    : "bg-gray-800 text-gray-300 hover:bg-gray-700 hover:text-white"
                }`}
                onClick={() => setActiveTab(tab)}
              >
                {tab
                  .split("-")
                  .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
                  .join(" & ")}
              </button>
            ))}
          </div>
        </div>

        {/* Tool Grid */}
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          {filteredAndSortedTools.map((tool) => (
            <div
              key={tool.id}
              className="bg-gray-800/40 backdrop-blur-sm border border-gray-700 rounded-xl shadow-lg p-6 flex flex-col h-full hover:shadow-2xl hover:border-blue-500/50 transition-all duration-300"
            >
              <div className="flex items-center mb-4">
                <div className="text-2xl mr-3">{tool.logo}</div>
                <h3 className="text-xl font-semibold text-white">{tool.name}</h3>
              </div>
              <p className="text-gray-300 mb-4">{tool.shortDescription}</p>

              <div className="flex-grow">
                <p className="text-gray-400">{tool.description}</p>
              </div>

              <div className="mt-4">
                <span
                  className={`inline-flex items-center px-3 py-1 rounded-full text-sm font-medium ${getPricingColor(
                    tool.pricing,
                  )}`}
                >
                  {tool.pricing}
                </span>
              </div>

              <div className="mt-6 flex justify-between items-center">
                <div className="text-yellow-500 font-medium">
                  {tool.rating} ({tool.users})
                </div>
                <a
                  href={`https://${tool.domain}`}
                  target="_blank"
                  rel="noopener noreferrer"
                  className="group bg-blue-600 hover:bg-blue-700 text-white p-2 rounded-full transition-all duration-300 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-offset-gray-800/40 focus:ring-blue-500"
                  aria-label={`Visit ${tool.name}`}
                >
                  <ArrowUpRight className="h-5 w-5 transition-transform group-hover:rotate-45" />
                </a>
              </div>
            </div>
          ))}
        </div>
      </div>
    </section>
  )
}

export { ExploreSection }
export default ExploreSection
